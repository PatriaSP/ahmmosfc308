public class Sfc308VoPart {
    private String partnum;
    private String engineno;
    private String qty;
    private String seq;
    private String type;
    private String idcasepos;
    private String vitemcodecasepos;

    public String getPartnum() {
        return partnum;
    }

    public void setPartnum(String partnum) {
        this.partnum = partnum;
    }

    public String getEngineno() {
        return engineno;
    }

    public void setEngineno(String engineno) {
        this.engineno = engineno;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getSeq() {
        return seq;
    }

    public void setSeq(String seq) {
        this.seq = seq;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getIdcasepos() {
        return idcasepos;
    }

    public void setIdcasepos(String idcasepos) {
        this.idcasepos = idcasepos;
    }

    public String getVitemcodecasepos() {
        return vitemcodecasepos;
    }

    public void setVitemcodecasepos(String vitemcodecasepos) {
        this.vitemcodecasepos = vitemcodecasepos;
    }
    
}
